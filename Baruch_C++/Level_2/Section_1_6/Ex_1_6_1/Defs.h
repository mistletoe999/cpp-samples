// Defs.h contains the definitions for Macros PRINT1 and PRINT2

#ifndef DEFS_H_
#define DEFS_H_

// PRINT1 MACRO: Prints the value of a single variable
#define PRINT1(a) printf("The variable value is %f\n", a)
// PRINT2 MACRO: Prints the value of two variables
#define PRINT2(a, b) (printf("The variable values are %f, %f\n", a, b))

#endif //DEFS_H_